package com.model;

/**
 *
 * @author ZAKARIA
 */
public class Kegemukan {
    Long id;
    String nama;

    public Kegemukan(Long id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
    
    
}
